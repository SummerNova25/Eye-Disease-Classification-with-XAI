def shap_explain(model, img_path, val_ds, label_names,
                 num_background=5, target_size=(256, 256), nsamples=25):

    # ---------------------------
    # 1. Load image (256x256)
    # ---------------------------
    img = tf.keras.preprocessing.image.load_img(img_path, target_size=target_size)
    img_array = tf.keras.preprocessing.image.img_to_array(img) / 255.0
    img_input = np.expand_dims(img_array, axis=0)

    # ---------------------------
    # 2. Get predicted class (using 512)
    # ---------------------------
    preds = model.predict(tf.image.resize(img_input, (512, 512)), verbose=0)
    pred_class = int(np.argmax(preds))

    # ---------------------------
    # 3. Prepare background (IMPORTANT: resize to 256)
    # ---------------------------
    background = []
    for images, _ in val_ds.take(2):
        for img in images:
            img_resized = tf.image.resize(img, target_size)
            background.append(img_resized.numpy())

            if len(background) >= num_background:
                break
        if len(background) >= num_background:
            break

    background = np.array(background[:num_background])

    # ---------------------------
    # 4. Flatten for SHAP
    # ---------------------------
    bg_flat = background.reshape(num_background, -1)
    img_flat = img_input.reshape(1, -1)

    # ---------------------------
    # 5. Prediction function
    # ---------------------------
    def predict_fn(x_flat):
        x = x_flat.reshape(-1, target_size[0], target_size[1], 3)
        x_resized = tf.image.resize(x, (512, 512))

        preds = model.predict(x_resized, verbose=0)

        # ensure 2D
        if len(preds.shape) == 1:
            preds = np.expand_dims(preds, axis=0)

        return preds[:, pred_class].reshape(-1)

    # ---------------------------
    # 6. SHAP explainer
    # ---------------------------
    explainer = shap.KernelExplainer(predict_fn, bg_flat)
    shap_values = explainer.shap_values(img_flat, nsamples=nsamples)

    # ---------------------------
    # 7. Reshape for visualization
    # ---------------------------
    shap_img = shap_values.reshape(1, target_size[0], target_size[1], 3)
    img_show = img_input.reshape(1, target_size[0], target_size[1], 3)

    # ---------------------------
    # 8. Plot
    # ---------------------------
    print(f"Predicted class: {label_names[pred_class]}")
    shap.image_plot(shap_img, img_show, labels=[label_names[pred_class]])

    return shap_values, pred_class

