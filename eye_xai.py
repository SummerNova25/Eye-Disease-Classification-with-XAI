def make_gradcam_heatmap(img_array, full_model, conv_layer_name, pred_index=None):
    import tf_keras
    import numpy as np

    base_model = full_model.layers[0]
    conv_layer = base_model.get_layer(conv_layer_name)

    # Build grad model using ONLY tf_keras
    grad_model = tf_keras.Model(
        inputs=base_model.input,
        outputs=[conv_layer.output, base_model.output]
    )

    img_tensor = tf.constant(img_array, dtype=tf.float32)

    with tf.GradientTape() as tape:
        tape.watch(img_tensor)
        conv_outputs, base_out = grad_model(img_tensor, training=False)
        tape.watch(conv_outputs)

        # Manually run the head layers (after base_model)
        x = conv_outputs
        # Actually run full prediction using base_out through head
        head_input = base_out
        for layer in full_model.layers[1:]:  # GAP, Dense, Dropout, Dense
            head_input = layer(head_input, training=False)
        predictions = head_input

        if pred_index is None:
            pred_index = int(tf.argmax(predictions[0]))
        class_channel = predictions[:, pred_index]

    grads = tape.gradient(class_channel, conv_outputs)
    pooled_grads = tf.reduce_mean(grads, axis=(0, 1, 2))
    heatmap = conv_outputs[0] @ pooled_grads[..., tf.newaxis]
    heatmap = tf.squeeze(heatmap)
    heatmap = tf.maximum(heatmap, 0) / (tf.math.reduce_max(heatmap) + 1e-8)

    return heatmap.numpy()

